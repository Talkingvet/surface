export function getTheme(id: string): Theme {
  return THEMES.find((t) => t.id === id) ?? THEMES[0];
}

export const FONTS: { id: string; name: string; stack: string }[] = [
  { id: 'instrument', name: 'Instrument', stack: "'Instrument Sans', sans-serif" },
  { id: 'inter', name: 'Inter', stack: "'Inter', sans-serif" },
  { id: 'system', name: 'System', stack: "system-ui, 'Segoe UI', sans-serif" },
  { id: 'mono', name: 'Mono', stack: "'Spline Sans Mono', monospace" },
];

export function applyAppearance(opts: {
  themeId: string;
  fontId: string;
  fontScale: number;
  uiScale: number;
}): void {
  const theme = getTheme(opts.themeId);
  const root = document.documentElement;
  for (const [k, v] of Object.entries(theme.vars)) root.style.setProperty(k, v);
  root.style.colorScheme = theme.kind;
  const font = FONTS.find((f) => f.id === opts.fontId) ?? FONTS[0];
  root.style.setProperty('--font-ui', font.stack);
  root.style.fontSize = `${16 * opts.fontScale}px`;
  (document.body.style as CSSStyleDeclaration & { zoom: string }).zoom = String(opts.uiScale);
}
